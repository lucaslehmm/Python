## Parte 2 - Tuplas
# Tupla(tuple) - As tuplas são coleções ordenadas e imutáveis. Uma vez criada, você não pode alterar seus elementos.

cores = ('Vermelho', "Azul", "Roxo")

# Metodos  uteis
# index(item) - Retorna a posição do item.
print(cores.index('Azul'))

# count(item) - Conta quantas vezes o item aparece.
print(cores.count('Azul'))