# Vogais em uma palavra:
# Peça ao usuário para digitar uma palavra e use o for para imprimir apenas as vogais da palavra.

# palavra = input('Digite uma palavra: ')

palavra = "leonardo" # e o a o

for vogal in palavra:
    if vogal == "a":
        print(vogal)
    elif vogal == "e":
        print(vogal)
    elif vogal == "i":
        print(vogal)
    elif vogal == "o":
        print(vogal)
    elif vogal == "u":
        print(vogal)
