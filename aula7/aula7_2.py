import string

# string - A biblioteca string fornece conjuntos prontos de caracteres, como letras e símbolos.

# Comandos principais

# 1. string.ascii_letters → Retorna todas as letras (maiúsculas e minúsculas).

print(string.ascii_letters)

# 2. string.ascii_lowercase → Apenas letras minúsculas.

print(string.ascii_lowercase)

# 3. string.ascii_uppercase → Apenas letras maiúsculas.]

print(string.ascii_uppercase)

# 4. string.digits → Retorna os números de 0 a 9.

print(string.digits)

# 5. string.punctuation → Retorna símbolos como !@#$%&.

print (string.punctuation)